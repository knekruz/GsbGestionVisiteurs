    private void btnInsererMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsererMouseClicked
        // TODO add your handling code here:
        if(tblVisiteurs.getSelectedRowCount() == 0)
        {
         JOptionPane.showMessageDialog(null,
                    "Veuillez sélectionner un visiteur", "Erreur de saisie",
                    JOptionPane.ERROR_MESSAGE);   
        }
        else if(txtRole.getText().compareTo("") == 0)
        {
            // On affiche un message
            JOptionPane.showMessageDialog(null,
                    "Veuillez saisir un Role", "Erreur de saisie",
                    JOptionPane.ERROR_MESSAGE);
        }
        else if(txtDate.getDate() ==null)
        {
            // On affiche un message
            JOptionPane.showMessageDialog(null,
                    "Veuillez saisir une date", "Erreur de saisie",
                    JOptionPane.ERROR_MESSAGE);
        }
        
        else 
        {
            int mat = Integer.parseInt(tblVisiteurs.getValueAt(tblVisiteurs.getSelectedRow(), 0).toString());
           
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd"); 
            String dateEmb = format.format( txtDate.getDate() );
            
            int region = fm.getIdRegionByName(cboRegion.getSelectedItem().toString()); // On recuper l'id de la region
            String role = txtRole.getText();
            fm.insertTravailler(mat, dateEmb, region, role);
            
            mdlTravailler.loadDatas(fm.getAllTravailleurs());
            // On met à jour le modèle du JTable
            tblVisiteursRegion.setModel(mdlTravailler);
        }
            
    }//GEN-LAST:event_btnInsererMouseClicked

